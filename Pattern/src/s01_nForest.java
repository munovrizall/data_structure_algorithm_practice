public class s01_nForest {

    public static void main(String[] args) {
        int loop = 3;
        for (int i = 0; i < loop; i++) {
            for (int j = 0; j < loop; j++) {
                System.out.print("* ");
            }
            System.out.println();
        }
    }
}
